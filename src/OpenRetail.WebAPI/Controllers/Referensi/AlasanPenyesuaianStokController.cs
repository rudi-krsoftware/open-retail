                _response = Content(_httpStatusCode, output);
            }
            catch (Exception ex)
            {
                if (_log != null)
                    _log.Error("Error:", ex);
            }

            return _response;
        }

        [HttpPost, Route("delete")]
        public IHttpActionResult Delete(AlasanPenyesuaianStokDTO objDTO)
        {
            _httpStatusCode = HttpStatusCode.BadRequest;
